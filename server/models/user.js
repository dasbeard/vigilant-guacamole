// =========================================================================
// ============================== Require ==================================
// =========================================================================
const mongoose = require('mongoose');


// =========================================================================
// ============================== Schemas ==================================
// =========================================================================
var UserSchema = new mongoose.Schema({
  name:{type: String, required: true, minlength: 3},

},{timestamps: true});

// =========================================================================
// ========================== Set Schema Name ==============================
// =========================================================================
mongoose.model('User', UserSchema);
